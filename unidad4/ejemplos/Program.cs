char letra = 'O';

switch(letra){
    case 'M' :
        Console.WriteLine("Tu nombre comienza con M...");
        break;
    case 'N' :
        Console.WriteLine("Tu nombre comienza con N...");
        break;
    case 'A' :
        Console.WriteLine("Tu nombre comienza con A...");
        break;
    case 'J' :
        Console.WriteLine("Tu nombre comienza con J...");
        break;
    default:
        Console.WriteLine("Tu nombre no coincide con la base de datos");
        break;
}